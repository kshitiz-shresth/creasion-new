                                                <h4>The Challenge</h4>
                                                <p>In 2017/18, the unemployment rate among youth was found to be 21.4% which was 10% more than the national average of 11.4%.  Despite 5.6 million youth population between 15-24 years, 2 million were not in employment, education or training. The statistics indicate a major lag between the human resource present in Nepal and our inability to engage them in a better way. CREASION identifies the serious lack of exposure to and change of job opportunities among young citizens in general. To address this need, Empower+ was launched.    </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Intervention</h4>
                                                <p>Empower+ is an effort for the youth that provides them with necessary skills and trainings required to enter the work force and start their professional career. Through Empower+, VFC hosts a series of trainings, workshops and hand-on sessions for students to help them learn skills that complements their college education. We aim to be able to provide youth with the right mentorship and trainings that will be instrumental for them to create a future that they desire.   </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Process</h4>
                                                <p>Empower+ is spread over a total of three days with meticulously planned training programs as per the need of the young students. The standard Empower+ training sessions consist of CV and Cover Letter Writing, Report Writing, Microsoft Office Package, Interview Tips and Techniques and Email Etiquette. The trainings sessions are also accompanied by experience sharing by professionals along with personality development class by experts. The sessions are customised according the to the need, request and proficiency of the students on the selected programs.</p>

                                                <p>Besides the three- day program, we have also introduced a one-day Empower+ session which is basically a 2-3 hours long experience sharing style workshops about topics ranging from leadership to public speaking.  </p>

