                                                <h4>The Challenge</h4>
                                                <p>The role of waste workers is often undermined in our society considering the nature of their profession. However, without their presence and their contribution, the environment and the society would fail to be in balance. It is important to recognize the contribution of our waste workers and provide them with respect and assistance. The perpetual injustice and lack of attention to their well- being is unfair to them as well as a risk for the environment and society. Hence, Hamro Cycle Yatra was launched as a means to acknowledge and show respect to the contributions of waste workers.    </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Intervention</h4>
                                                <p>Hamro Cycle Yatra is an initiative to acknowledge the contributions of our sanitation workers who perform an important service in our society. It is a symbolic way to publicly thank our sanitation workers and bring their work in highlight for everybody to appreciate. The event consists of public acknowledgement through rally as well as health check-ups to ensure their well- being.      </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Process</h4>
                                                <p>Hamro Cycle Yatra is organized on the occasion of the World Enviornment Day every year in the month of June. Volunteers, professionals from other fields gather together to participate in the rally which is joined by waste workers as well. The rally is accompanied by health check-up for the waste workers followed by other fun events.   </p>

                                        
