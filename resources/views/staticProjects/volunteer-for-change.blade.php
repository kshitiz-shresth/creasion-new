                                                <h4>The Challenge</h4>
                                                <p>The society tends to form stereotypes, internalize them and inject them in the minds and hearts of people. When the society presents us with an idea or opinion about certain issues, rarely do we question it and tend to go along with the original perspective. This has created a lot of misconceptions on important issues and directly and indirectly also influenced our actions- often invoking a negative impact.  Recognizing the prevalence of so many misinterpreted issues, CREASION launched Debunking Myths.  </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Intervention</h4>
                                                <p>Debunking Myths does not point judgment passed in the history and in the present as right or wrong but simply wants to instigate a certain level of curiosity in the audience as to learn, explore and challenge the existing narrative. It is a series devoted towards bringing light into the myths that have been ingrained in our society and influenced our way of life and perceptions. It carefully chooses themes that have the ability to strike a conversation about misplaced issues and topics and debunk age-old myths that have dictated our lives and choices.       </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Process</h4>
                                                <p>Debunking Myths decides on a theme that has been misinterpreted in the past and invites a panel of experts and keynote speakers who then go on to dissect the themes and enlighten the audience with a clear concept of the issue at discussion. If the themes pique the interest of the audience then we also conduct follow- up events to let our audience have a clear picture of the issue at hand.   </p>

                                        
