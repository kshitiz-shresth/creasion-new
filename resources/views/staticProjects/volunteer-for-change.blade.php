                                                <h4>The Challenge</h4>
                                                <p>With increasing modernization and industrialization, humans seem to be getting more and more disconnected from the nature. Our concept of development is generally defined by high- rise buildings and high economic growth and in the midst of it, we have been jeopardizing the natural resources present around us and undermining their importance. Development cannot attain its true essence until and unless we find a balance between the economic and natural environment.     </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Intervention</h4>
                                                <p>Tree of the Year Nepal was introduced as an initiative to make people realize the importance of biodiversity and the natural environment. It is an extension of the Asian Tree of the Year  organized by Carbon Consulting Company (CCC), South Asiaâs foremost provider in integrated sustainability services. Tree of the Year Nepal is affiliated to the Asian Tree of the Year and is a national contest that focuses on starting a conversation amongst the citizens on the importance of protecting trees and the importance to preserve biodiversity. As Nepal stands in the cross- roads of development, the contest aims to provide a clarity to the citizens on what should be prioritised in the path to achieving sustainable development.        </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Process</h4>
                                                <p>Tree of the Year Nepal is basically a photography contest that welcomes photo entries of trees that have special histories. The photograph entries must be accompanied by a story pertaining to why the tree is of special significance. The judges evaluate the winners on the basis of not just the quality of the photograph but also on the basis of the significance of the tree and the entrant's portrayal of it to instigate the need to conserve biodiversity present around us.     </p>

                                        
