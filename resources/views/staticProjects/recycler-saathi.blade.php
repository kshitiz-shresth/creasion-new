                                                <p>The engagement of youth and children through youth clubs and Waste Smart Clubs has been initiated by Recycler Saathi. It believes in harnessing the power and enthusiasm of the young minds to bring change and influence the community that they live in and eventually attract greater momentum to the cause of recycling. Further, other stakeholders through partnerships are also engaged in the process of outreach and advocacy.   </p>
                                            </div>

                                            <div class="id-detail">
