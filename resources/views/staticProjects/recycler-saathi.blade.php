                                                <p>Waste Smart Clubs are mostly focused in government schools and has been engaging young students to understand the environment better. Similarly, youth have been engaged in the process through various voluntary activities. Recycler Saathi has also engaged the various groups of community through both physical and digital mediums. Through physical medium, it organizes and partakes in rallies, talk shows and stakeholders meeting. From digital platforms, it ensures awareness and engagement through PSAs, informative videos and posts.     </p>

