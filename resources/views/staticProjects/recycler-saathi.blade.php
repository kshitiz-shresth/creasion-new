                                                <h4>The Challenge</h4>
                                                <p>The greater goal of Recycler Saathi cannot be achieved in isolation. There is need for awareness among a larger mass of people, which we tend to lack. Especially the engagement of young students and children in actions for environment conservation is minimal and needs to be expanded. Without ground level realization from the community itself, no substantial change can be implemented. Further, policy level support which is imperative to inculcate and institutionalise any change is highly lacking in Nepal is terms of waste management.     </p>
