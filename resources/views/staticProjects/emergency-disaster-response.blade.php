                                                <h4>The Challenge</h4>
                                                <p>The COVID-19 outbreak has dismantled sense of normalcy from the world. Our lives have been toppled and the economy, environment, society is at its lowest. Amidst this crisis, many groups of people have a struggle earning their daily livelihood. Waste workers are one such community of people who still need to work in the front line despite the threat of the virus but whose contributions have not been well recognized. They still face stigma attached to their profession as well as struggle to sustain their livelihood in the wake of COVID-19.     </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Intervention</h4>
                                                <p>The Waste Workers Emergency Relief Project (WWERP) was introduced in response to the challenges faced by waste workers due to COVID-19. It is an initiative for waste workers to ease their livelihood and ensure their safety while at work. The plan of action of the project involved provision of ration materials, awareness sessions, safety gears and free medical consultations to waste workers and their families so their survival was easy and without risk even when their profession demanded them to be on the ground despite the risks of the virus.     </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Process</h4>
                                                <p>The waste workers of Kathmandu, Lalitpur, Bhaktapur, Chitwan, Parsa, Sarlahi and Saptari were reached out through WWERP. The list of waste workers who would receive the assistance was curated with the help of our consortium partners- Doko Recyclers, NEPSEMYAK, Khaalisisi, Blue Waste to Value and Recycler Saathi. As per the plan and by following the rules of social distancing they were provided with the assistance materials and awareness sessions. The waste workers who were stationed at hospitals were given special priority and provided with awareness sessions on proper hospital waste management.     </p>

                                                <p>
                                                    In support of <strong>The Bhrikuti Devi Trust</strong>, <strong>15 hospitals</strong> from Province 1,2,3,4 and 7 were also provided with safety gears.  A total of <strong>887 PPE Suits, 1174 N95 Masks, 1300 pairs of Surgical Gloves, 140 Safety Goggles, 749 Shoe Covers and 749 Hair Covers</strong> were given to the front- line workers stationed at hospitals. Other front liners including security personnel were also assisted by providing safety gears.  
