                                                <h4>The Challenge</h4>
                                                <p>Flooding in Nepal is an annual occurrence that wreaks havoc in many communities. Reports suggest that since 1990 more than 10000 casualties have occurred every year in the six districts- Dhading, Makwanpur, Rautahat, Sarlahi, Sindhuli and Chitwan. The intensity of disasters especially in the Terai region only seems to be aggravating. Despite the repetition of the same pattern every year, the efforts to prevent this mishap is very bleak.   </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Intervention</h4>
                                                <p>Realizing the need for a collaborative effort to tackle the impacts of the flood especially since the 2017 Flood, Alliance for Disaster Relief was launched. It is an alliance of youth clubs, vloggers, students, disaster experts, ex-armies and volunteers and serves as a common platform for those who want to help the affected communities. It was created under the leadership of CREASION and served as a reliable platform to dispatch help when needed for flood- affected communities.   </p>
                                            </div>

                                            <div class="id-detail">
                                                <h4>The Process</h4>
                                                <p>Alliance for Disaster Relief is a reliable platform to collect in-kind donations from different interested donors and business enterprises and disseminate them to the neediest communities affected by flood. During the 2017 flood, CREASION’s head office served as the main centre of communication where real time information on the disaster- hit area were effectively collected from Nepal Police and the local people. Birgunj was the main logistic hub where the materials were transported to various affected areas. Every year as in the 2017 flood, CREASION steps on the front line and acts as a bridge to dispatch relief for disaster affected communities.    </p>
